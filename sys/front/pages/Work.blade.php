                                               <h3>Evaluation of the Two-generation Approach to ECD – Kulea Watoto
                                                   project.
                                               </h3>
                                               <p style="height: 150px; overflow-y:scroll">
                                                   The AfriChild Centre is the research and learning partner for a
                                                   consortium of 5 organizations under the Kulea Watoto (Nurturing
                                                   Children) Project. The consortium is led by the International Rescue
                                                   Committee (IRC), Kabarole Research and Resource Centre, The AfriChild
                                                   Centre, Literacy and Adult Basic Education, and The Madrasa Early
                                                   Childhood Programme.

                                                   The goal of the project is to improve the well-being and school
                                                   readiness of children aged 0 to 5 years in refugee and host
                                                   communities in Yumbe, Kyegegwa, and Kampala. The 3-year project
                                                   (2023-2025) employs a two-generation approach that simultaneously
                                                   targets both parents/caregivers and the children. As a research and
                                                   learning partner, The AfriChild Centre is generating evidence to
                                                   support the improvement, expansion and sustainability of integrated
                                                   Early Childhood Development and livelihoods programming.
                                                   Specifically, the research and learning will generate evidence on
                                                   which programme models work for a two-generation approach that
                                                   improves early learning and responsive caregiving at the household
                                                   level. Additionally, the centre’s research explores which models
                                                   address economic vulnerabilities and improve household incomes and
                                                   livelihood opportunities for both refugees and host communities. The
                                                   Centre is using complementary approaches to learn and will conduct
                                                   outcome and process evaluations across all project implementation and
                                                   control sites.




