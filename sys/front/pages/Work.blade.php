                                               <h3>Research and Learning Partnership with NRC, for ECD and Livelihoods
                                                   Project

                                               </h3>
                                               <p style="height: 150px; overflow-y:scroll">
                                                   The AfriChild Centre is working with the Norwegian Refugee Council
                                                   (NRC) in Uganda to conduct research and learning for the NRC's Early
                                                   Childhood Care and Development (ECCD) and Livelihoods Project. The
                                                   project is being implemented in the Southwestern refugee settlements
                                                   of Isingiro and Kyangwali, as well as with the urban refugees of
                                                   Kampala in Uganda.



                                                   The study will generate research and learning evidence on
                                                   humanitarian interventions that employ the dual generation approach
                                                   by focusing on skilling interventions for youth and early childhood
                                                   care (both formal and informal) for their children. The findings from
                                                   this study will be used to guide the development of early childhood
                                                   care and development (ECCD) programming in humanitarian settings.
                                                   Moreover, it will inform policymakers on how to best integrate ECCD
                                                   into whole-family programming within the context of the humanitarian
                                                   setting.

                                                   The 20-month-long project began on November 1, 2023, and involves
                                                   multiple stakeholders. The Ministry of Gender, Labour, and Social
                                                   Development (MoGLSD) is leading the implementation of ECCD at the
                                                   national level. This is based on the integrated early childhood and
                                                   development (IECD) policy and frameworks.

