                                               <h3>Playful Futures




                                                   <br>

                                               </h3>
                                               <p style="height: 150px; overflow-y:scroll">
                                                   The AfriChild Centre received funding from Plan International-Uganda
                                                   to conduct a baseline assessment for the Playful Futures project.
                                                   Playful Futures (PF) aims to develop effective, scalable approaches
                                                   for engaging educators and communities around Early Childhood Care
                                                   and Education (ECCE) centres in support of Learning through Play
                                                   (LtP). The baseline study was conducted in 80 Early Child Care and
                                                   Education Centers in 6 districts of Alebtong, Lira, Kamuli, Buyende,
                                                   Tororo and Nebbi. Study Objective The overall goal of the project is
                                                   “Children improve literacy, and numeracy skills and develop
                                                   socio-emotional skills through Learning through Play. Research
                                                   QuestionTo what extent does the ECCE intervention improve quality and
                                                   sustained learning through play and holistic development among
                                                   children 3-6 years in centre-based learning environments?



                                               </p>
