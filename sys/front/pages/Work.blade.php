                                               <h3>Mapping Child Protection and Honour Study



                                                   <br>

                                               </h3>
                                               <p style="height: 150px; overflow-y:scroll">
                                                   The AfriChild Centre is partnering with the London School of Hygiene
                                                   and Tropical Medicine to explore how global narratives of child
                                                   protection and perspectives of honour affect child wellbeing. This is
                                                   part of a multi-country study taking place in Uganda, Sri Lanka, New
                                                   Zealand and South Africa. AfriChild is leading the Uganda study. This
                                                   study advances current scholarship on two topics related to honour:
                                                   1) honour as a factor in sustaining violence against children, and 2)
                                                   honour as a factor contributing to child well-being through
                                                   children’s social relationships (with family, peers, and community).
                                                   This research agenda is informed by both a systematic scoping review
                                                   of the literature on honour-related violence and children, and by a
                                                   meeting held in Washington DC in November 2019. In this meeting a
                                                   group of academics, researchers, funders and activists gathered to
                                                   reflect on the links between honour and child protection, and how
                                                   investigating these links can improve child protection programs and
                                                   policies. In this study, AfriChild is using an innovative youth
                                                   participatory approach to enhance children’s meaningful participation
                                                   in research. The youth are actively involved as peer researchers and
                                                   the Centre uses other participatory methods to enhance the research
                                                   experience.




                                               </p>
